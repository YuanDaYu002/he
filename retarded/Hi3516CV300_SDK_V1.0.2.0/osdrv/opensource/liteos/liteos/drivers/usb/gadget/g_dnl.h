#ifndef _G_DNL_H_
#define _G_DNL_H_

#include "implementation/freebsd_sys.h"
#include "composite.h"

/* from config descriptor bmAttributes */
#define USB_CONFIG_ATT_ONE			(1 << 7)	/* must be set */
#define USB_CONFIG_ATT_SELFPOWER	(1 << 6)	/* self powered */
#define USB_CONFIG_ATT_WAKEUP		(1 << 5)	/* can wakeup */
#define USB_CONFIG_ATT_BATTERY		(1 << 4)	/* battery powered */

bool g_usb_dnl_detach(void);
void g_usb_dnl_trigger_detach(void);
void g_usb_dnl_clear_detach(void);

struct string_des
{
    char *str;
    int len;
};

extern void set_manufacturer_string(struct string_des *des);
extern void set_serial_number_string(struct string_des *des);
extern void set_product_string(struct string_des *des);

#endif /* _G_DNL_H_ */
